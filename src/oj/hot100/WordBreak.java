package oj.hot100;

import java.util.List;

public class WordBreak {
    public boolean wordBreak(String s, List<String> wordDict) {
        boolean[] res = new boolean[s.length() + 1];
        res[0] = true;
        while (true) {

            break;
        }
        return res[res.length - 1];
    }
}
